export const translations: Record<LangCode, Dict> = { en, sk, cs, de, hu, fr, es }

export function t(lang: LangCode, key: string): string {
  const keys = key.split('.')
  let node: Dict | string = translations[lang] ?? translations.en
  for (const k of keys) {
    if (typeof node !== 'object') return key
    node = (node as Dict)[k] ?? ''
  }
  if (!node) {
    // fallback to English
    let fb: Dict | string = translations.en
    for (const k of keys) {
      if (typeof fb !== 'object') return key
      fb = (fb as Dict)[k] ?? ''
    }
    return typeof fb === 'string' && fb ? fb : key
  }
  return typeof node === 'string' ? node : key
}
